func spin(delay time.Duration, done chan bool, spinner []string) {
	for {
		select {
		case <-done:
			return
		default:
			for _, r := range spinner {
				fmt.Printf("\r%s", r)
				time.Sleep(delay)
			}
		}
	}

}
func main() {
	done := make(chan bool)
	for key := range spinners {
		go spin(time.Duration(200*time.Millisecond), done, spinners[key])
		time.Sleep(2 * time.Second)
		done <- true
		fmt.Println("")
	}
}
